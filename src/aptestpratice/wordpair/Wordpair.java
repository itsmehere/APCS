package aptestpratice.wordpair;

public class Wordpair {
    String firstWord;
    String secondWord;

    public Wordpair(String w1, String w2){
        this.firstWord = w1;
        this.secondWord = w2;
    }

    public String getFirstWord(){
        return firstWord;
    }

    public String getSecondWord(){
        return secondWord;
    }

}
